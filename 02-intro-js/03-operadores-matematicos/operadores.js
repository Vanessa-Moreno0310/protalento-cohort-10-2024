console.log("** Operadores Matemáticos **");

// Suma +
console.log(5 + 8);
console.log(5.3 + 1.8);
console.log(NaN + NaN);

// Resta
console.log(55 - 18);
console.log(5.3 - 1.8);

// Multiplicación
console.log(55 * 18);
console.log(5.3 * 1.8);

// División
console.log(55 / 18);
console.log(5.3 / 1.8);
console.log(1/3);
console.log((1/3).toFixed(3));  // limitar los decimale

// Residuo/Módulo (%)
// Los que sobra de una división
console.log(5 % 3);
console.log(16 % 3.5);
// Es divisible entre 3 (o es un múltiplo de 3)
console.log(15 % 3);  // 0 si es divisible
console.log(153333332 % 3);  // si es divisible

// un número es par si:
// Si un número es múltiplo de 2
// al dividirlo entre 2 el residuo es 0
console.log(23 % 2);  // 0 ***

// Exponencial (**) Las potencias de algún número
console.log(2 ** 5);  // 2 * 2 * 2 * 2  * 2
console.log(2 ** 5);